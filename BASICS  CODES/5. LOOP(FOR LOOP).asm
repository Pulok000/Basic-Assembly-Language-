.MODEL SMALL
.STACK 100H
.DATA
 
MSG1 DB "HELLO WORLD",0DH,0AH,24H

.CODE

MAIN PROC
    
    MOV AX,@DATA
    MOV DS,AX
    

    ; FOR CREATING A COUNTER FOR FOR LOOP
    MOV CX,07H
    
    
    ;FOR MESSAGE(20-29).ITS A FOR LOOP  
    START:
    

    
    LEA DX,MSG1
    MOV AH,09H
    INT 21H 
 

    LOOP START
     
    

    EXIT:
    MOV AH,4CH
    INT 21H
    
    MAIN ENDP
END MAIN 

